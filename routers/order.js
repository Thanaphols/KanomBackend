const express = require("express")
const router = express.Router()
const orderController = require('../controllers/orderControllers')
const Middlewere = require('../middleware/validate')
router.get('/',Middlewere.validateToken,orderController.getallOrder)
router.get('/getOrder/:o_ID',Middlewere.validateToken,orderController.getOrderID)
router.post('/addOrder',Middlewere.validateToken,orderController.addOrder)
router.patch('/verifyDeposit',Middlewere.validateToken,orderController.verifyDeposit)
router.patch('/updateStatusOrder',Middlewere.validateToken,orderController.updateStatusOrder)
router.delete('/deleteOrder/:o_ID',Middlewere.validateToken,orderController.deleteOrders)
router.get(`/ordersitems/:o_ID`,Middlewere.validateToken,orderController.orderitems)
router.get('/getdateEnd/:o_ID',Middlewere.validateToken,orderController.getdateEnd)
router.patch(`/updateOrder`,Middlewere.validateToken,orderController.updateOrder)
router.get('/sumPrice/:o_ID',Middlewere.validateToken,orderController.sumPrice)
router.get('/getUserOrders',Middlewere.validateToken,orderController.getUserOrders)
router.delete('/cancelOrder/:o_ID',Middlewere.validateToken,orderController.cancelOrder)
module.exports = router